                   ASSUME  CS:CODE   DS:DATA
DATA SEGMENT
x	DB 	02H 	;less than 6D
n	DB 	03H	;less than 12D or 0C H
Op1	 DW 	01	 DUP(?)
RESULT	 DW 	02	 DUP(?)
DATA ENDS

CODE SEGMENT
START:
MOV AX, DATA
MOV DS, AX

MOV CL,x
CMP CL,00H
JA L6  
MOV CL,n
CMP CL,01H
JB L7

L6: MOV CL,n
CMP CL, 06H
MOV BX, 0001H
JB L1

MOV CH , n
SUB CH , 06H
MOV AL, x
MOV BL, x
XOR AH ,AH
XOR DX ,DX
XOR BH ,BH

L3: DEC CH
    JZ L2
 	MUL BX
 	JMP L3

L2:	LEA DI , op1
	MOV [DI] ,AX

MOV CL, 06H
MOV AL, 01H
MOV BL, x
XOR AH,AH
XOR DX,DX
XOR BH,BH

L4:	MUL BX
	DEC CL
JNZ L4

MOV BX , AX
LEA DI, op1
MOV AX , [DI]

MUL BX
JMP OUT

L1:	MOV AL,01H
	MOV BL,x
	XOR AH,AH
	XOR BH,BH

L5:	MUL BX
	DEC CL
	JNZ L5
	XOR DX ,DX   
JMP OUT
L7: MOV AX,9999H
    MOV DX,9999H

OUT:	LEA BX , RESULT
	MOV [BX],AX
	MOV 02H[BX] , DX     
	

CODE ENDS
END START
RET
